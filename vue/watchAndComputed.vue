计算属性: 
1. 对于任何复杂的逻辑,都应当使用计算属性(若将同一函数定义为一个方法而不是一个计算属性,两种方法的最终结果是完全相同的,不同的是计算属性是基于他们的依赖进行缓存的,只有当相关历史发生改变时他们才会重新求值.);
2. 计算属性是用来声明式的描述一个值依赖了其他的值,当依赖的值发生改变时,其值才会相应的发生更新并更新相关dom
3. 相比之下,每当触发重新渲染时,调用方法将总会再次执行函数.
4. 计算属性中只要属性的数据没发生变化,就可以直接返回缓存里的数据,而不需要每次都重新执行数据操作

侦听属性:
1. 大多情况计算属性更试用,但有时也需要一个自定义的侦听器,这就是为什么vue通过watch选项提供一个更通用的方法,来响应数据的变化.
2. 使用watch有一个特点:当值第一次绑定时,不会执行监听函数,只有值发生改变才会执行.
如果需要在最初绑定时也执行函数,需要用到immediate属性